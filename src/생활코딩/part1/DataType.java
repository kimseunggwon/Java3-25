package 생활코딩.part1;

public class DataType {
	public static void main(StringApp[] args) {
		System.out.println(6); // Number 숫자
		System.out.println("six"); // String 문자

		System.out.println("6"); // String 6
		                         //사람이 보기엔 숫자이지만 큰 따음표를 사용함으로 컴퓨터 한테는 문자가 된다.
		
		System.out.println(6+6); //12
		System.out.println("6"+"6"); // 66
		
		System.out.println(6*6); //36
//		System.out.println("6"*"6"); //문자열 데이터 타입은 곱하기 불가능
		
		System.out.println("1111".length()); //길이 4
		System.out.println(1111); 
	}
}
