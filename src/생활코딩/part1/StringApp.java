package 생활코딩.part1;

public class StringApp {
	public static void main(String[] args) {
		System.out.println("Hi"); //String 문자열 (Charter 캐릭터들이 모여 있는것)
		System.out.println("H"); //Chracter 특징 한문자를 표현하는것
		//문자와 문자열은 다르다.
		
		System.out.println("Hi");
		
		//역슬래쉬n 이면 문자열 안에서 줄띄어쓰기가 가능하다
		System.out.println("Hello \nWorld");
		
		//역슬래쉬 큰따음표를 묶으면 Hello "World" 묶인곳만 큰 따음표 출력이 가능하다
		System.out.println("Hello \"World\"");
	}

}
